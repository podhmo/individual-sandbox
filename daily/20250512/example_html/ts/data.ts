/**
 * 個々のテンプレートの型定義
 */
export interface Template {
  id: string; // テンプレートの一意識別子
  name: string; // テンプレートタイトル
  template: string; // テンプレート本文 (変数を含む)
  variables: string[]; // テンプレート内の変数名の配列
  description?: string; // テンプレートの説明 (オプション)
}

/**
 * カテゴリとそれに属するテンプレート群の型定義
 */
export interface CategoryTemplates {
  categoryName: string; // カテゴリ名
  templates: Template[]; // このカテゴリに属するテンプレートの配列
}

