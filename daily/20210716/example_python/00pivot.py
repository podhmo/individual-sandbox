import pandas as pd

data = [
    {"A": 1, "B": 1, "x": 1, "y": 1},
    {"A": 1, "B": 2, "x": 10, "y": 10},
    {"A": 1, "B": 3, "x": 100, "y": 100},
    {"A": 2, "B": 1, "x": 1, "y": 1},
    {"A": 2, "B": 2, "x": 10, "y": 10},
    {"A": 2, "B": 3, "x": 100, "y": 100},
    {"A": 3, "B": 1, "x": 1, "y": 1},
    {"A": 3, "B": 2, "x": 10, "y": 10},
    {"A": 3, "B": 3, "x": 100, "y": 100},
]

df = pd.DataFrame.from_records(data)
print(df)
#    A  B    x    y
# 0  1  1    1    1
# 1  1  2   10   10
# 2  1  3  100  100
# 3  2  1    1    1
# 4  2  2   10   10
# 5  2  3  100  100
# 6  3  1    1    1
# 7  3  2   10   10
# 8  3  3  100  100

df2 = df.pivot(index="A", columns="B", values="x")
print(df2)
# B  1   2    3
# A
# 1  1  10  100
